const int maxnode = 30000 + 5;
const int maxedge = 2100000 + 5;
const int oo = 1000000000;
int node, src, dest, nedge;
int head[maxnode], point[maxedge], nxt[maxedge], flow[maxedge],
capa[maxedge];
int dist[maxnode], Q[maxnode], work[maxnode];

void init(int _node, int _src, int _dest) {
	node = _node;
	src = _src;
	dest = _dest;
	for (int i = 0; i < node; i++)
		head[i] = -1;
	nedge = 0;
}
void addedge(int u, int v, int c1, int c2) {
	point[nedge] = v, capa[nedge] = c1, flow[nedge] = 0,
	nxt[nedge] = head[u], head[u] =(nedge++);
	point[nedge] = u, capa[nedge] = c2, flow[nedge] = 0,
	nxt[nedge] = head[v], head[v] =(nedge++);
}
bool dinic_bfs() {
	memset(dist, 255, sizeof(dist));
	dist[src] = 0;
	int sizeQ = 0;
	Q[sizeQ++] = src;
	for (int cl = 0; cl < sizeQ; cl++)
		for (int k = Q[cl], i = head[k]; i >= 0; i = nxt[i])
			if (flow[i] < capa[i] && dist[point[i]] < 0) {
				dist[point[i]] = dist[k] + 1;
				Q[sizeQ++] = point[i];
			}
	return dist[dest] >= 0;
}
int dinic_dfs(int x, int exp) {
	if (x == dest)
		return exp;
	for (int &i = work[x]; i >= 0; i = nxt[i]) {
		int v = point[i], tmp;
		if (flow[i] < capa[i] && dist[v] == dist[x] + 1 && (tmp = dinic_dfs(v, min(exp, capa[i] - flow[i]))) > 0) {
			flow[i] += tmp;
			flow[i ^ 1] -= tmp;
			return tmp;
		}
	}
	return 0;
}
int dinic_flow() {
	int result = 0;
	while (dinic_bfs()) {
		for (int i = 0; i < node; i++)
			work[i] = head[i];
		while (1) {
			int delta = dinic_dfs(src, oo);
			if (delta == 0)
				break;
			result += delta;
		}
	}
	return result;
}

int n;
vector<pair<pi,int> > E;

void generate(){
	vector<vi> L;
	int depth = rand()%5 + 1;
	n = 0;
	for(int i = 0;i <= depth;i++){
		L.pb(vi());
		int q = rand()%3 + 1;
		while(q--) L[i].pb(n++);
	}
	E.clear();
	for(int i = 0;i < depth;i++){
		int m = rand()%3 + 1;
		while(m--){
			int x = L[i][rand()%sz(L[i])];
			int y = L[i+1][rand()%sz(L[i + 1])];
			E.pb(mp(mp(x,y),rand()%10 + 1));
		}
	}
}

int main(){
	srand(time(0));
	for(int i = 0;i < 10;i++){
		generate();
		init(n,0,n-1);
		for(auto e : E) addedge(e.xx.xx,e.xx.yy,e.yy,0);

		int old = dinic_flow();
		init(n,0,n-1);
		for(auto e : E) addedge(e.xx.xx,e.xx.yy,(sz(E) + 1)*e.yy + 1,0);
		int NEW = dinic_flow();
		cout << n << " " << sz(E)  << " " << old << " " << NEW << " \t " << NEW - old*(sz(E) + 1) << endl;
	}
	return 0;
}
